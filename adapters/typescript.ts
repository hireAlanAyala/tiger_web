// TypeScript adapter — reads the annotation manifest from the scanner,
// extracts function names from source files, and generates the dispatch.
//
// Usage: npx tsx adapters/typescript.ts generated/manifest.json generated/dispatch.generated.ts
//
// New protocol: JSON length-prefixed frames over unix socket.
// Four handler phases: route, prefetch, handle, render.
// Prefetch is declarative SQL — sidecar sends queries, framework executes.
// Handle queues writes via db.execute() — SQL sent back to framework.
// No binary serde. No PrefetchCache. No WriteSlot.

import { readFileSync, writeFileSync } from "fs";

const manifestPath = process.argv[2];
const outputPath = process.argv[3];

if (!manifestPath || !outputPath) {
  console.error("Usage: npx tsx adapters/typescript.ts <manifest.json> <output.ts>");
  process.exit(1);
}

interface ManifestAnnotation {
  phase: "translate" | "prefetch" | "execute" | "render";
  operation: string;
  file: string;
  line: number;
}

interface ResolvedAnnotation extends ManifestAnnotation {
  func_name: string;
}

// Read manifest.
const manifest: { annotations: ManifestAnnotation[] } = JSON.parse(
  readFileSync(manifestPath, "utf-8")
);

// Extract function names from source files.
const resolved: ResolvedAnnotation[] = [];

for (const ann of manifest.annotations) {
  const lines = readFileSync(ann.file, "utf-8").split("\n");
  let codeLine: string | null = null;
  for (let i = ann.line; i < lines.length; i++) {
    const line = lines[i].trim();
    if (line === "") continue;
    if (line.startsWith("//") || line.startsWith("#") || line.startsWith("--")) continue;
    codeLine = lines[i];
    break;
  }

  if (!codeLine) {
    console.error(`error: ${ann.file}:${ann.line}: no code line found after annotation`);
    process.exit(1);
  }

  const funcName = extractFuncName(codeLine);
  if (!funcName) {
    console.error(`error: ${ann.file}:${ann.line}: cannot extract function name from: ${codeLine.trim()}`);
    process.exit(1);
  }

  resolved.push({ ...ann, func_name: funcName });
}

function extractFuncName(line: string): string | null {
  const trimmed = line.trim();
  const funcMatch = trimmed.match(
    /^(?:export\s+)?(?:async\s+)?function\s+([a-zA-Z_]\w*)/
  );
  if (funcMatch) return funcMatch[1];
  const constMatch = trimmed.match(/^(?:export\s+)?const\s+([a-zA-Z_]\w*)/);
  if (constMatch) return constMatch[1];
  return null;
}

// Build per-operation module map: operation → { file, namespace }.
// Each handler file exports route/prefetch/handle/render with the same names,
// so we use namespace imports (import * as X from '...') to avoid collisions.
const operationModules = new Map<string, { file: string; namespace: string }>();

function toCamelCase(op: string): string {
  return op.replace(/_([a-z])/g, (_, c) => c.toUpperCase());
}

for (const ann of resolved) {
  if (!operationModules.has(ann.operation)) {
    operationModules.set(ann.operation, {
      file: ann.file,
      namespace: toCamelCase(ann.operation),
    });
  }
}

// Build dispatch tables per phase.
const phases = ["translate", "prefetch", "execute", "render"] as const;
const tables: Record<string, Map<string, string>> = {};
for (const phase of phases) {
  tables[phase] = new Map();
  for (const ann of resolved.filter((a) => a.phase === phase)) {
    const mod = operationModules.get(ann.operation)!;
    tables[phase].set(ann.operation, `${mod.namespace}.${ann.func_name}`);
  }
}

// Generate output.
let out = `// Auto-generated by the TypeScript adapter — do not edit.
//
// Sidecar dispatch — binary protocol over unix socket.
// Four handler phases: route → prefetch → handle → render.
// Three round trips: RT1 (route+prefetch decl), RT2 (results+handle), RT3 (render+HTML).
// Handle queues writes via db.execute().

import * as net from 'net';
import { unlinkSync } from 'fs';

`;

// Namespace imports — one per operation handler file.
for (const [_op, mod] of operationModules) {
  const relative = "../" + mod.file;
  out += `import * as ${mod.namespace} from '${relative}';\n`;
}

out += "\n";

// Dispatch tables.
for (const phase of phases) {
  const tableName = phase === "translate" ? "routeHandlers"
    : phase === "prefetch" ? "prefetchHandlers"
    : phase === "execute" ? "handleHandlers"
    : "renderHandlers";
  out += `const ${tableName}: Record<string, Function> = {\n`;
  for (const [op, funcName] of tables[phase]) {
    out += `  '${op}': ${funcName},\n`;
  }
  out += "};\n\n";
}

// Socket server with binary length-prefixed protocol.
out += `// --- Binary protocol ---
// Wire format: [4-byte BE length][u8 message_tag][payload]
// Three round trips per request:
//   RT1: route_request → route_prefetch_response
//   RT2: prefetch_results → handle_render_response
//   RT3: render_results → html_response

import {
  readRowSet, writeParams, writeSqlDeclarations, writeWriteQueue,
  MessageTag, TypeTag, frame_max,
  type SqlDeclaration, type WriteEntry,
} from './serde.ts';
import { OperationValues, StatusValues, OperationNames } from './types.generated.ts';

function sendFrame(conn: net.Socket, payload: Uint8Array): void {
  const header = Buffer.alloc(4);
  header.writeUInt32BE(payload.length, 0);
  conn.write(header);
  conn.write(payload);
}

const _decoder = new TextDecoder();
const _encoder = new TextEncoder();

const socketPath = process.argv[2];
if (!socketPath) { console.error('Usage: node dispatch.generated.ts <socket-path>'); process.exit(1); }

// Frame buffer — allocated once, reused per request. Avoids GC pressure.
// frame_max imported from serde.ts — cross-language verified by serde_test.ts.
const frameBuf = new ArrayBuffer(frame_max);
const frameDv = new DataView(frameBuf);

const server = net.createServer((conn) => {
  console.log('[sidecar] client connected');
  let pending = Buffer.alloc(0);

  // Per-request state — stored between RT1 and RT2/RT3.
  let routeBody: Record<string, any> = {};
  let routeOperation = '';
  let routeId = '';
  let prefetchKeys: string[] = [];
  let prefetchModes: Record<string, string> = {};

  function processFrames(): void {
    while (pending.length >= 4) {
      const frameLen = pending.readUInt32BE(0);
      if (pending.length < 4 + frameLen) break;
      const frame = new Uint8Array(pending.buffer, pending.byteOffset + 4, frameLen);
      pending = pending.subarray(4 + frameLen);
      if (frame.length < 1) { conn.destroy(); return; }
      handleFrame(frame);
    }
  }

  function handleFrame(frame: Uint8Array): void {
    const tag = frame[0];
    const dv = new DataView(frame.buffer, frame.byteOffset, frame.byteLength);

    if (tag === MessageTag.route_request) {
      // RT1: route_request → route_prefetch_response
      // Parse: [tag][method][u16 path_len][path][u16 body_len][body]
      let pos = 1;
      const method = frame[pos]; pos += 1;
      const pathLen = dv.getUint16(pos, false); pos += 2;
      const path = _decoder.decode(frame.subarray(pos, pos + pathLen)); pos += pathLen;
      const bodyLen = dv.getUint16(pos, false); pos += 2;
      const body = _decoder.decode(frame.subarray(pos, pos + bodyLen));

      const methods = ['', 'get', 'post', 'put', 'delete'];
      const req = { method: methods[method] || 'get', path, body, params: {} };

      // Try each route handler.
      let result: any = null;
      for (const handler of Object.values(routeHandlers)) {
        const r = handler(req);
        if (r !== null) { result = r; break; }
      }

      if (!result) {
        // Not found: [tag][found=0]
        sendFrame(conn, new Uint8Array([MessageTag.route_prefetch_response, 0]));
        return;
      }

      routeOperation = result.operation;
      routeBody = result.body || {};
      routeId = String(result.id).padStart(32, '0');

      // Validate operation exists in dispatch tables.
      if (!(routeOperation in handleHandlers) || !(routeOperation in renderHandlers)) {
        console.error('[sidecar] operation not in dispatch tables:', routeOperation);
        sendFrame(conn, new Uint8Array([MessageTag.route_prefetch_response, 0]));
        return;
      }

      // Call prefetch handler — get SQL declarations. Store keys for RT2.
      const prefetchFn = prefetchHandlers[routeOperation];
      const prefetchMsg = { operation: routeOperation, id: routeId, body: routeBody };

      // PrefetchDb — records query declarations. Only query and queryAll
      // are available. No execute, no writes. The handler calls db.query()
      // which returns a PrefetchQuery declaration, not a result.
      const db = {
        query: (sql: string, ...params: unknown[]) => ({ sql, params, mode: "query" as const }),
        queryAll: (sql: string, ...params: unknown[]) => ({ sql, params, mode: "queryAll" as const }),
      };
      const queries: Record<string, any> = prefetchFn ? prefetchFn(prefetchMsg, db) : {};

      prefetchKeys = Object.keys(queries);
      prefetchModes = {};
      const decls: SqlDeclaration[] = prefetchKeys.map(key => {
        const q = queries[key];
        prefetchModes[key] = q.mode || 'query';
        return { key, sql: q.sql, mode: q.mode || 'query', params: q.params || [] };
      });

      // Build response into reusable frame buffer.
      let rpos = 0;
      frameDv.setUint8(rpos, MessageTag.route_prefetch_response); rpos += 1;
      frameDv.setUint8(rpos, 1); rpos += 1; // found
      frameDv.setUint8(rpos, OperationValues[routeOperation] || 0); rpos += 1;

      // ID as u128 BE (16 bytes from hex string).
      for (let i = 0; i < 16; i++) {
        frameDv.setUint8(rpos + i, parseInt(routeId.substring(i * 2, i * 2 + 2), 16));
      }
      rpos += 16;

      rpos += writeSqlDeclarations(frameDv, rpos, decls);
      sendFrame(conn, new Uint8Array(frameBuf, 0, rpos));

    } else if (tag === MessageTag.prefetch_results) {
      // RT2: prefetch_results → handle_render_response
      // Parse row sets using stored keys from RT1 (no re-call).
      let pos = 1; // skip tag

      const prefetched: Record<string, any> = {};
      for (const key of prefetchKeys) {
        if (pos >= frame.length) break;
        const { result: rowSet, offset: newPos } = readRowSet(dv, pos);
        pos = newPos;
        if (prefetchModes[key] === 'query') {
          prefetched[key] = rowSet.rows.length > 0 ? rowSet.rows[0] : null;
        } else {
          prefetched[key] = rowSet.rows;
        }
      }

      // Build handle context.
      const ctx = { operation: routeOperation, id: routeId, prefetched, body: routeBody };

      // Call handle — queues writes via db.execute().
      const writes: WriteEntry[] = [];
      const db = {
        execute(sql: string, ...params: unknown[]): void {
          writes.push({ sql, params });
        },
      };
      const handleFn = handleHandlers[routeOperation];
      const status: string = handleFn(ctx, db);

      // Render declarations (empty for now — render-with-db deferred).
      const renderDecls: SqlDeclaration[] = [];

      // Build response into reusable frame buffer.
      let rpos = 0;
      frameDv.setUint8(rpos, MessageTag.handle_render_response); rpos += 1;
      const statusByte = StatusValues[status];
      if (statusByte === undefined) throw new Error('unknown status from handle: ' + status);
      frameDv.setUint8(rpos, statusByte); rpos += 1;
      rpos += writeWriteQueue(frameDv, rpos, writes);
      rpos += writeSqlDeclarations(frameDv, rpos, renderDecls);
      sendFrame(conn, new Uint8Array(frameBuf, 0, rpos));

      // Store render context for RT3.
      const renderCtx = { ...ctx, status, is_sse: false };
      (conn as any)._renderCtx = renderCtx;

    } else if (tag === MessageTag.render_results) {
      // RT3: render_results → html_response
      // Render row sets will be parsed here when render-with-db is wired.
      const renderCtx = (conn as any)._renderCtx;
      const renderFn = renderHandlers[routeOperation];

      const html: string = renderFn ? renderFn(renderCtx) : '';
      const htmlBytes = _encoder.encode(html);

      // Build response: [tag][html_bytes]
      const respBuf = new Uint8Array(1 + htmlBytes.length);
      respBuf[0] = MessageTag.html_response;
      respBuf.set(htmlBytes, 1);
      sendFrame(conn, respBuf);

    } else {
      console.error('[sidecar] unknown tag:', tag);
      conn.destroy();
    }
  }

  conn.on('data', (chunk: Buffer) => {
    pending = Buffer.concat([pending, chunk]);
    processFrames();
  });
  conn.on('close', () => console.log('[sidecar] client disconnected'));
  conn.on('error', (err: Error) => console.error('[sidecar] error:', err.message));
});

try { unlinkSync(socketPath); } catch {}
server.listen(socketPath, () => console.log(\`[sidecar] listening on \${socketPath}\`));
process.on('SIGINT', () => { server.close(); process.exit(0); });
`;

writeFileSync(outputPath, out);
console.log(`Generated: ${outputPath}`);
