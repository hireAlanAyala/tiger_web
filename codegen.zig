fn int_str(comptime n: comptime_int) []const u8 {
    if (n == 0) return "0";
    var buf_local: [20]u8 = undefined;
    var pos: usize = 20;
    var val = if (n < 0) -n else n;
    while (val > 0) {
        pos -= 1;
        buf_local[pos] = @intCast('0' + val % 10);
        val = val / 10;
    }
    if (n < 0) {
        pos -= 1;
        buf_local[pos] = '-';
    }
    return buf_local[pos..20];
}

fn type_leaf_name(comptime T: type) []const u8 {
    const full = @typeName(T);
    var last_dot: usize = 0;
    for (full, 0..) |ch, i| {
        if (ch == '.') last_dot = i + 1;
    }
    const name = if (last_dot == 0) full else full[last_dot..];
    assert(name.len > 0);
    assert(is_ts_identifier(name));
    return name;
}

/// Returns true if s is a valid TypeScript identifier (ASCII letters, digits, underscores).
fn is_ts_identifier(comptime s: []const u8) bool {
    if (s.len == 0) return false;
    if (!((s[0] >= 'a' and s[0] <= 'z') or (s[0] >= 'A' and s[0] <= 'Z') or s[0] == '_')) return false;
    for (s[1..]) |ch| {
        if (!((ch >= 'a' and ch <= 'z') or (ch >= 'A' and ch <= 'Z') or (ch >= '0' and ch <= '9') or ch == '_')) return false;
    }
    return true;
}

fn is_array_type(comptime T: type) bool {
    return @typeInfo(T) == .array;
}

fn has_field(comptime StructT: type, comptime name: []const u8) bool {
    for (@typeInfo(StructT).@"struct".fields) |f| {
        if (std.mem.eql(u8, f.name, name)) return true;
    }
    return false;
}

/// Checks whether `field_name` has a length companion field in StructT.
/// Detects `{name}_len` and the `items` + `len` pattern.
/// Asserts the base field is actually an array — if not, the _len naming
/// convention is misleading and the codegen would produce wrong output.
fn has_len_companion(comptime StructT: type, comptime field_name: []const u8) bool {
    for (@typeInfo(StructT).@"struct".fields) |f| {
        if (f.name.len == field_name.len + 4 and
            std.mem.eql(u8, f.name[0..field_name.len], field_name) and
            std.mem.eql(u8, f.name[field_name.len..], "_len"))
        {
            assert(is_array_field(StructT, field_name));
            return true;
        }
    }
    if (std.mem.eql(u8, field_name, "items") and has_field(StructT, "len")) {
        assert(is_array_field(StructT, "items"));
        return true;
    }
    return false;
}

/// Offset and bit-width of a _len companion field, used by serde emitters
/// to generate the correct DataView read/write call.
const LenInfo = struct {
    offset: usize,
    bits: u16,
};

/// Returns the offset and type of the _len companion for a given field.
/// Caller must have verified has_len_companion() returns true.
/// Asserts the companion field is an unsigned integer (u8, u16, or u32).
fn get_len_info(comptime StructT: type, comptime field_name: []const u8) LenInfo {
    for (@typeInfo(StructT).@"struct".fields) |f| {
        if (f.name.len == field_name.len + 4 and
            std.mem.eql(u8, f.name[0..field_name.len], field_name) and
            std.mem.eql(u8, f.name[field_name.len..], "_len"))
        {
            assert(@typeInfo(f.type) == .int);
            assert(@typeInfo(f.type).int.signedness == .unsigned);
            return .{ .offset = @offsetOf(StructT, f.name), .bits = @typeInfo(f.type).int.bits };
        }
    }
    // items + len pattern
    if (std.mem.eql(u8, field_name, "items")) {
        for (@typeInfo(StructT).@"struct".fields) |f| {
            if (std.mem.eql(u8, f.name, "len")) {
                assert(@typeInfo(f.type) == .int);
                assert(@typeInfo(f.type).int.signedness == .unsigned);
                return .{ .offset = @offsetOf(StructT, f.name), .bits = @typeInfo(f.type).int.bits };
            }
        }
    }
    unreachable;
}

/// Returns true if the named field in StructT is an array type.
fn is_array_field(comptime StructT: type, comptime field_name: []const u8) bool {
    for (@typeInfo(StructT).@"struct".fields) |f| {
        if (std.mem.eql(u8, f.name, field_name)) return is_array_type(f.type);
    }
    return false;
}

/// Returns true if the field name is a reserved or padding region (not a _len companion).
fn is_reserved_or_padding(comptime field_name: []const u8) bool {
    return std.mem.startsWith(u8, field_name, "reserved") or std.mem.eql(u8, field_name, "padding");
}

/// Returns true if a field should be omitted from the TS output.
fn should_skip(comptime StructT: type, comptime field_name: []const u8) bool {
    if (std.mem.startsWith(u8, field_name, "reserved")) return true;
    if (std.mem.eql(u8, field_name, "padding")) return true;

    // Skip _len companions (e.g., "name_len" when "name" exists)
    if (field_name.len > 4 and std.mem.eql(u8, field_name[field_name.len - 4 ..], "_len")) {
        const base = field_name[0 .. field_name.len - 4];
        if (has_field(StructT, base)) return true;
    }

    // Skip bare "len" when there's an "items" field (list structs)
    if (std.mem.eql(u8, field_name, "len") and has_field(StructT, "items")) return true;

    return false;
}

/// Counts union variants whose type is an anonymous struct (not in is_known_struct).
fn count_anon_structs(comptime U: type) usize {
    var count: usize = 0;
    for (@typeInfo(U).@"union".fields) |field| {
        if (field.type == void) continue;
        if (@typeInfo(field.type) != .@"struct") continue;
        if (!is_known_struct(field.type)) count += 1;
    }
    return count;
}

/// Returns true if T is a struct in the known_structs list.
fn is_known_struct(comptime T: type) bool {
    comptime assert(known_structs.len == 32);
    comptime {
        for (known_structs) |K| assert(@typeInfo(K) == .@"struct");
    }
    inline for (known_structs) |K| {
        if (T == K) return true;
    }
    return false;
}

/// Returns true if T is an enum in the known_enums list.
fn is_known_enum(comptime T: type) bool {
    comptime assert(known_enums.len == 11);
    comptime {
        for (known_enums) |E| assert(@typeInfo(E) == .@"enum");
    }
    inline for (known_enums) |E| {
        if (T == E) return true;
    }
    return false;
}

/// Returns true if T is a union in the known_unions list.
fn is_known_union(comptime T: type) bool {
    comptime assert(known_unions.len == 2);
    comptime {
        for (known_unions) |U| assert(@typeInfo(U) == .@"union");
    }
    inline for (known_unions) |U| {
        if (T == U) return true;
    }
    return false;
}

/// Asserts no two emitted types produce the same TS name.
/// Checks across all categories: enums, structs, unions, anonymous interfaces.
fn assert_no_name_collisions() void {
    @setEvalBranchQuota(200000);
    // Struct leaf names are unique among themselves.
    inline for (known_structs) |K1| {
        var count: usize = 0;
        inline for (known_structs) |K2| {
            if (std.mem.eql(u8, type_leaf_name(K1), type_leaf_name(K2))) count += 1;
        }
        assert(count == 1);
    }

    // Enum leaf names are unique among themselves.
    inline for (known_enums) |E1| {
        var count: usize = 0;
        inline for (known_enums) |E2| {
            if (std.mem.eql(u8, type_leaf_name(E1), type_leaf_name(E2))) count += 1;
        }
        assert(count == 1);
    }

    // No enum name collides with a struct name.
    inline for (known_enums) |E| {
        inline for (known_structs) |S| {
            assert(!std.mem.eql(u8, type_leaf_name(E), type_leaf_name(S)));
        }
    }

    // No union name collides with an enum or struct name.
    inline for (known_unions) |U| {
        inline for (known_enums) |E| {
            assert(!std.mem.eql(u8, type_leaf_name(U), type_leaf_name(E)));
        }
        inline for (known_structs) |S| {
            assert(!std.mem.eql(u8, type_leaf_name(U), type_leaf_name(S)));
        }
    }

    // Every enum/union/struct type referenced as a field is in the known lists.
    // If someone adds a new type to message.zig and uses it in a struct field
    // without adding it to the appropriate known list, this assertion catches it.
    inline for (known_structs) |S| {
        inline for (@typeInfo(S).@"struct".fields) |field| {
            if (@typeInfo(field.type) == .@"enum") {
                assert(is_known_enum(field.type));
            }
            if (@typeInfo(field.type) == .@"union") {
                assert(is_known_union(field.type));
            }
            // Nested struct fields (e.g., CollectionWithProducts.products: ProductList)
            // must be in known_structs so the serde generates readX/writeX for them.
            if (@typeInfo(field.type) == .@"struct") {
                assert(is_known_struct(field.type));
            }
            // Check optional children (e.g., ?FollowupState).
            if (@typeInfo(field.type) == .optional) {
                const child = @typeInfo(field.type).optional.child;
                if (@typeInfo(child) == .@"enum") assert(is_known_enum(child));
                if (@typeInfo(child) == .@"union") assert(is_known_union(child));
                if (@typeInfo(child) == .@"struct") assert(is_known_struct(child));
            }
        }
    }
}

