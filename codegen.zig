test "is_known_struct covers all union variant types" {
    // Every non-void, non-primitive Result variant type is known.
    comptime {
        for (@typeInfo(message.Result).@"union".fields) |field| {
            if (field.type == void) continue;
            if (@typeInfo(field.type) == .int) continue;
            assert(is_known_struct(field.type));
        }
    }

    // Every non-void Write variant is either known or anonymous (struct).
    comptime {
        for (@typeInfo(SM.Write).@"union".fields) |field| {
            if (field.type == void) continue;
            const info = @typeInfo(field.type);
            if (info == .@"struct") continue;
            assert(info == .int or info == .bool or info == .@"enum");
        }
    }
}

