test "int_str decimal formatting" {
    comptime assert(std.mem.eql(u8, int_str(0), "0"));
    comptime assert(std.mem.eql(u8, int_str(7), "7"));
    comptime assert(std.mem.eql(u8, int_str(42), "42"));
    comptime assert(std.mem.eql(u8, int_str(672), "672"));
    comptime assert(std.mem.eql(u8, int_str(-1), "-1"));
    comptime assert(std.mem.eql(u8, int_str(-60), "-60"));
    comptime assert(std.mem.eql(u8, int_str(message.body_max), "672"));
    comptime assert(std.mem.eql(u8, int_str(message.list_max), "50"));
    comptime assert(std.mem.eql(u8, int_str(SM.writes_max), "21"));
    comptime assert(std.mem.eql(u8, int_str(@sizeOf(message.Message)), "880"));
}

test "should_skip field filtering" {
    // Reserved fields — always skipped.
    comptime assert(should_skip(message.ProductCollection, "reserved"));
    comptime assert(should_skip(message.InventoryTransfer, "reserved"));
    comptime assert(should_skip(message.Message, "reserved"));

    // Padding — always skipped.
    comptime assert(should_skip(message.ProductFlags, "padding"));

    // _len companions — skipped when base field exists.
    comptime assert(should_skip(message.Product, "name_len"));
    comptime assert(should_skip(message.Product, "description_len"));
    comptime assert(should_skip(message.OrderResult, "items_len"));
    comptime assert(should_skip(message.OrderResult, "payment_ref_len"));
    comptime assert(should_skip(message.Message, "credential_len"));

    // Bare "len" — skipped when "items" exists.
    comptime assert(should_skip(message.ProductList, "len"));
    comptime assert(should_skip(message.CollectionList, "len"));
    comptime assert(should_skip(message.OrderSummaryList, "len"));

    // items_len on OrderSummary — NOT a companion (no "items" array field).
    comptime assert(!should_skip(message.OrderSummary, "items_len"));

    // Regular fields — never skipped.
    comptime assert(!should_skip(message.Product, "id"));
    comptime assert(!should_skip(message.Product, "name"));
    comptime assert(!should_skip(message.Product, "price_cents"));
    comptime assert(!should_skip(message.Product, "flags"));
    comptime assert(!should_skip(message.OrderResult, "status"));
    comptime assert(!should_skip(message.Message, "operation"));
    comptime assert(!should_skip(message.Message, "body"));
}

test "has_len_companion detection" {
    // Byte arrays with _len.
    comptime assert(has_len_companion(message.Product, "name"));
    comptime assert(has_len_companion(message.Product, "description"));
    comptime assert(has_len_companion(message.OrderCompletion, "payment_ref"));
    comptime assert(has_len_companion(message.Message, "credential"));
    comptime assert(has_len_companion(message.LoginCodeRequest, "email"));

    // Typed arrays with _len.
    comptime assert(has_len_companion(message.OrderRequest, "items"));
    comptime assert(has_len_companion(message.OrderResult, "items"));

    // items + len pattern (list structs).
    comptime assert(has_len_companion(message.ProductList, "items"));
    comptime assert(has_len_companion(message.CollectionList, "items"));

    // Fields without companions.
    comptime assert(!has_len_companion(message.Product, "id"));
    comptime assert(!has_len_companion(message.Product, "price_cents"));
