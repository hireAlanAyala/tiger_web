const output = blk: {
    @setEvalBranchQuota(100000);
    var buf: [65536]u8 = undefined;
    var w = Writer{ .buf = &buf, .pos = 0 };

    w.raw("// Auto-generated by `zig build codegen` — do not edit.\n\n");

    // --- Constants ---
    w.emit_const("body_max", message.body_max);
    w.emit_const("list_max", message.list_max);
    w.emit_const("dashboard_list_max", message.dashboard_list_max);
    w.emit_const("order_items_max", message.order_items_max);
    w.emit_const("product_name_max", message.product_name_max);
    w.emit_const("product_description_max", message.product_description_max);
    w.emit_const("collection_name_max", message.collection_name_max);
    w.emit_const("search_query_max", message.search_query_max);
    w.emit_const("email_max", message.email_max);
    w.emit_const("code_length", message.code_length);
    w.emit_const("payment_ref_max", message.payment_ref_max);
    w.emit_const("order_timeout_seconds", message.order_timeout_seconds);
    w.emit_const("credential_max", message.Message.credential_max);
    w.emit_const("writes_max", SM.writes_max);
    w.emit_const("path_max", protocol.path_max);
    w.emit_const("json_body_max", protocol.json_body_max);
    w.raw("\n");

    // --- Enums ---
    for (known_enums) |T| w.emit_enum(T);

    // --- Structs (dispatch by layout) ---
    for (known_structs) |T| {
        const layout = @typeInfo(T).@"struct".layout;
        if (layout == .@"packed") {
            w.emit_packed_struct(T);
        } else if (layout == .@"extern") {
            w.emit_extern_struct(T);
        } else {
            w.emit_struct(T);
        }
    }

    // --- Anonymous struct interfaces for union variants ---
    for (known_unions) |U| w.emit_union_anon_structs(U);

    // --- Tagged unions ---
    for (known_unions) |U| w.emit_union(U);

    // --- Serde: binary ↔ TypeScript serialization ---
    w.emit_serde_helpers();

    for (known_structs) |T| {
        const layout = @typeInfo(T).@"struct".layout;
        if (layout == .@"packed") {
            w.emit_packed_serde(T);
        } else if (layout == .@"extern") {
            w.emit_extern_serde(T);
        }
    }

    // --- Exhaustiveness assertions ---

    // Union variant counts — if a variant is added, codegen must be updated.
    assert(@typeInfo(message.Result).@"union".fields.len == 10);
    assert(@typeInfo(SM.Write).@"union".fields.len == 11);

    // Anon struct counts — ties emit_union_anon_structs to emit_union.
    assert(count_anon_structs(SM.Write) == 3);
    assert(count_anon_structs(message.Result) == 0);

    // Every Operation.EventType references a type we handle.
    for (@typeInfo(message.Operation).@"enum".fields) |op_field| {
        const op: message.Operation = @enumFromInt(op_field.value);
        const ET = op.EventType();
        if (ET != void) {
            assert(is_known_struct(ET) or @typeInfo(ET) == .int);
        }
    }

    // No two emitted types produce the same TS name.
    assert_no_name_collisions();

    // Buffer headroom — assert we used less than 75% to catch bloat early.
    assert(w.pos > 0);
    assert(w.pos < buf.len * 3 / 4);

    const final = buf[0..w.pos];
    break :blk final[0..final.len].*;
};

pub fn main() !void {
    try std.io.getStdOut().writer().writeAll(&output);
}

// ---------------------------------------------------------------------------
// Writer — comptime string builder
// ---------------------------------------------------------------------------

const Writer = struct {
    buf: *[65536]u8,
    pos: usize,

    fn raw(w: *Writer, s: []const u8) void {
        assert(w.pos + s.len <= w.buf.len);
        @memcpy(w.buf[w.pos..][0..s.len], s);
        w.pos += s.len;
    }

    fn write_pascal(w: *Writer, comptime name: []const u8) void {
        assert(name.len > 0);
        var cap = true;
        for (name) |ch| {
            if (ch == '_') {
                cap = true;
            } else {
                assert(w.pos < w.buf.len);
                w.buf[w.pos] = if (cap and ch >= 'a' and ch <= 'z') ch - 32 else ch;
                w.pos += 1;
                cap = false;
            }
        }
    }

    fn int(w: *Writer, comptime n: comptime_int) void {
        w.raw(int_str(n));
    }

    // --- Constants ---

    fn emit_const(w: *Writer, comptime name: []const u8, comptime value: comptime_int) void {
        w.raw("export const ");
        w.raw(name);
        w.raw(" = ");
        w.int(value);
        w.raw(";\n");
    }

    // --- Enums ---
    // Name derived from type — no free string parameter.

    fn emit_enum(w: *Writer, comptime T: type) void {
        const name = type_leaf_name(T);
        const info = @typeInfo(T).@"enum";
        const fields = info.fields;
        assert(fields.len > 0);
        // Serde uses dv.getUint8/setUint8 — enum must fit in one byte.
        assert(@sizeOf(T) == 1);

        w.raw("export type ");
        w.raw(name);
        w.raw(" =\n");
        for (fields) |field| {
            assert(is_ts_identifier(field.name));
            w.raw("  | \"");
            w.raw(field.name);
            w.raw("\"\n");
        }
        w.raw("  ;\n\n");

        w.raw("export const ");
        w.raw(name);
        w.raw("Values = {\n");
        for (fields) |field| {
            w.raw("  ");
            w.raw(field.name);
            w.raw(": ");
            w.int(field.value);
            w.raw(",\n");
        }
        w.raw("} as const;\n\n");
    }

    // --- Packed flag structs ---

    fn emit_packed_struct(w: *Writer, comptime T: type) void {
        const name = type_leaf_name(T);
        assert(@typeInfo(T).@"struct".layout == .@"packed");

        w.raw("export interface ");
        w.raw(name);
        w.raw(" {\n");
        var bool_count: usize = 0;
        for (@typeInfo(T).@"struct".fields) |field| {
            if (std.mem.eql(u8, field.name, "padding")) continue;
            if (field.type != bool) continue;
            bool_count += 1;
            w.raw("  ");
            w.raw(field.name);
            w.raw(": boolean;\n");
        }
        assert(bool_count > 0);
        w.raw("}\n\n");
    }

    // --- Extern structs (with @offset/@size JSDoc) ---

    fn emit_extern_struct(w: *Writer, comptime T: type) void {
        const name = type_leaf_name(T);
        const info = @typeInfo(T).@"struct";
        assert(info.layout == .@"extern");
        assert(stdx.no_padding(T));

        w.raw("/** @size ");
        w.int(@sizeOf(T));
        w.raw(" */\n");
        w.raw("export interface ");
        w.raw(name);
        w.raw(" {\n");

        var field_count: usize = 0;
        for (info.fields) |field| {
            if (should_skip(T, field.name)) continue;
            field_count += 1;

            // JSDoc with offset (and @max for arrays with _len companion)
            w.raw("  /** @offset ");
            w.int(@offsetOf(T, field.name));
            if (is_array_type(field.type) and has_len_companion(T, field.name)) {
                w.raw(" @max ");
                w.int(@typeInfo(field.type).array.len);
            }
            w.raw(" */\n");

            w.emit_field_decl(T, field);
        }
        assert(field_count > 0);

        w.raw("}\n\n");
    }

    // --- Regular structs (no offset JSDoc) ---
    // Asserts the type is NOT extern or packed — those have dedicated emit
    // functions. Calling this on an extern struct would silently drop @offset
    // JSDoc that the serde layer depends on.

    fn emit_struct(w: *Writer, comptime T: type) void {
        const name = type_leaf_name(T);
        const layout = @typeInfo(T).@"struct".layout;
        assert(layout != .@"extern");
        assert(layout != .@"packed");

        w.raw("export interface ");
        w.raw(name);
        w.raw(" {\n");

        for (@typeInfo(T).@"struct".fields) |field| {
            if (should_skip(T, field.name)) continue;

            if (is_array_type(field.type) and has_len_companion(T, field.name)) {
                w.raw("  /** @max ");
                w.int(@typeInfo(field.type).array.len);
                w.raw(" */\n");
            }

            w.emit_field_decl(T, field);
        }

        w.raw("}\n\n");
    }

    // --- Shared field declaration ---

    fn emit_field_decl(w: *Writer, comptime StructT: type, comptime field: std.builtin.Type.StructField) void {
        w.raw("  ");
        w.raw(field.name);
        w.raw(": ");
        w.write_field_type(StructT, field);
        w.raw(";\n");
    }

    fn write_field_type(w: *Writer, comptime StructT: type, comptime field: std.builtin.Type.StructField) void {
        if (is_array_type(field.type)) {
            const arr = @typeInfo(field.type).array;
            if (arr.child != u8) {
                // [N]T where T != u8 → T[] regardless of _len companion.
                w.write_ts_type(arr.child);
                w.raw("[]");
            } else if (has_len_companion(StructT, field.name)) {
                // [N]u8 with _len → string.
                w.raw("string");
            } else {
                // [N]u8 without _len → Uint8Array.
                w.raw("Uint8Array");
            }
            return;
        }
        w.write_ts_type(field.type);
    }

    fn write_ts_type(w: *Writer, comptime T: type) void {
        switch (@typeInfo(T)) {
            .int => |i| {
                if (i.bits == 128) w.raw("string") else w.raw("number");
            },
            .bool => w.raw("boolean"),
            .void => w.raw("void"),
            .@"enum" => w.raw(type_leaf_name(T)),
            .@"struct" => w.raw(type_leaf_name(T)),
            .@"union" => w.raw(type_leaf_name(T)),
            .optional => |opt| {
                w.write_ts_type(opt.child);
                w.raw(" | null");
            },
            else => @compileError("unhandled type: " ++ @typeName(T)),
        }
    }

    // --- Anonymous struct interfaces for union variants ---

    fn emit_union_anon_structs(w: *Writer, comptime U: type) void {
        const prefix = type_leaf_name(U);
        for (@typeInfo(U).@"union".fields) |field| {
            if (field.type == void) continue;
            if (@typeInfo(field.type) != .@"struct") continue;
            if (is_known_struct(field.type)) continue;

            w.raw("export interface ");
            w.raw(prefix);
            w.write_pascal(field.name);
            w.raw(" {\n");

            for (@typeInfo(field.type).@"struct".fields) |sf| {
                if (should_skip(field.type, sf.name)) continue;

                if (is_array_type(sf.type) and has_len_companion(field.type, sf.name)) {
                    w.raw("  /** @max ");
                    w.int(@typeInfo(sf.type).array.len);
                    w.raw(" */\n");
                }

                w.emit_field_decl(field.type, sf);
            }

            w.raw("}\n\n");
        }
    }

    // --- Tagged unions ---

    fn emit_union(w: *Writer, comptime U: type) void {
        const name = type_leaf_name(U);
        w.raw("export type ");
        w.raw(name);
        w.raw(" =\n");

        for (@typeInfo(U).@"union".fields) |field| {
            w.raw("  | { tag: \"");
            w.raw(field.name);
            w.raw("\"");
            if (field.type != void) {
                w.raw("; value: ");
                w.write_variant_type(U, field.name, field.type);
            }
            w.raw(" }\n");
        }

        w.raw("  ;\n\n");
    }

    /// Variant type reference — delegates to write_ts_type for all known types.
    /// Only overrides the struct branch for anonymous structs, which get a
    /// generated name (union prefix + PascalCase variant name).
    fn write_variant_type(w: *Writer, comptime U: type, comptime variant_name: []const u8, comptime T: type) void {
        if (@typeInfo(T) == .@"struct" and !is_known_struct(T)) {
            w.raw(type_leaf_name(U));
            w.write_pascal(variant_name);
        } else {
            w.write_ts_type(T);
        }
    }

    // =======================================================================
    // Serde — binary ↔ TypeScript serialization
    // =======================================================================

    fn emit_serde_helpers(w: *Writer) void {
        w.raw("// --- Serde ---\n\n");
        w.raw("const _decoder = new TextDecoder();\n");
        w.raw("const _encoder = new TextEncoder();\n\n");

        // readU128 / writeU128
        w.raw("function readU128(dv: DataView, offset: number): string {\n");
        w.raw("  const lo = dv.getBigUint64(offset, true);\n");
        w.raw("  const hi = dv.getBigUint64(offset + 8, true);\n");
        w.raw("  return ((hi << 64n) | lo).toString(16).padStart(32, '0');\n");
        w.raw("}\n\n");

        w.raw("function writeU128(dv: DataView, offset: number, hex: string): void {\n");
        w.raw("  const val = BigInt('0x' + (hex || '0'));\n");
        w.raw("  dv.setBigUint64(offset, val & 0xFFFFFFFFFFFFFFFFn, true);\n");
        w.raw("  dv.setBigUint64(offset + 8, val >> 64n, true);\n");
        w.raw("}\n\n");

        // readEnum / writeEnum
        w.raw("function readEnum<T extends string>(dv: DataView, offset: number, values: Record<string, number>): T {\n");
        w.raw("  const byte = dv.getUint8(offset);\n");
        w.raw("  for (const [name, val] of Object.entries(values)) {\n");
        w.raw("    if (val === byte) return name as T;\n");
        w.raw("  }\n");
        w.raw("  throw new Error('invalid enum value: ' + byte);\n");
        w.raw("}\n\n");

        w.raw("function writeEnum(dv: DataView, offset: number, values: Record<string, number>, name: string): void {\n");
        w.raw("  dv.setUint8(offset, values[name]);\n");
        w.raw("}\n\n");
    }

    fn emit_packed_serde(w: *Writer, comptime T: type) void {
        const name = type_leaf_name(T);
        const info = @typeInfo(T).@"struct";
        assert(info.layout == .@"packed");
        assert(@sizeOf(T) == 1); // Packed flags must fit in a single byte.
        const fields = info.fields;

        // Read function
        w.raw("function read");
        w.raw(name);
        w.raw("(dv: DataView, offset: number): ");
        w.raw(name);
        w.raw(" {\n  const byte = dv.getUint8(offset);\n  return {\n");
        for (fields) |field| {
            if (std.mem.eql(u8, field.name, "padding")) continue;
            if (field.type != bool) continue;
            w.raw("    ");
            w.raw(field.name);
            w.raw(": (byte & ");
            w.int(@as(u8, 1) << @bitOffsetOf(T, field.name));
            w.raw(") !== 0,\n");
        }
        w.raw("  };\n}\n\n");

        // Write function
        w.raw("function write");
        w.raw(name);
        w.raw("(dv: DataView, offset: number, val: ");
        w.raw(name);
        w.raw("): void {\n  dv.setUint8(offset, ");
        var first = true;
        for (fields) |field| {
            if (std.mem.eql(u8, field.name, "padding")) continue;
            if (field.type != bool) continue;
            if (!first) w.raw(" | ");
            first = false;
            w.raw("(val.");
            w.raw(field.name);
            w.raw(" ? ");
            w.int(@as(u8, 1) << @bitOffsetOf(T, field.name));
            w.raw(" : 0)");
        }
        w.raw(");\n}\n\n");
    }

    fn emit_extern_serde(w: *Writer, comptime T: type) void {
        const name = type_leaf_name(T);
        const info = @typeInfo(T).@"struct";
        assert(info.layout == .@"extern");
        assert(stdx.no_padding(T));
        const fields = info.fields;

        // --- Read function ---
        w.raw("export function read");
        w.raw(name);
        w.raw("(data: Uint8Array, offset: number): ");
        w.raw(name);
        w.raw(" {\n");
        w.raw("  const dv = new DataView(data.buffer, data.byteOffset, data.byteLength);\n");
        w.raw("  return {\n");
        for (fields) |field| {
            if (should_skip(T, field.name)) continue;
            w.emit_serde_read_field(T, field);
        }
        w.raw("  };\n}\n\n");

        // --- Write function ---
        // Each byte written exactly once. Reserved/padding regions zeroed.
        // _len companions written by their parent field — not zeroed here.
        // String/array buffers zeroed by emit_serde_write_field before encode.
        w.raw("export function write");
        w.raw(name);
        w.raw("(data: Uint8Array, offset: number, val: ");
        w.raw(name);
        w.raw("): void {\n");
        w.raw("  const dv = new DataView(data.buffer, data.byteOffset, data.byteLength);\n");
        for (fields) |field| {
            if (should_skip(T, field.name)) {
                // Only zero reserved/padding — _len fields are set by their
                // parent's write, so emitting a zero here would be a redundant write.
                if (is_reserved_or_padding(field.name)) {
                    w.raw("  data.fill(0, offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(", offset + ");
                    w.int(@offsetOf(T, field.name) + @sizeOf(field.type));
                    w.raw(");\n");
                }
                continue;
            }
            w.emit_serde_write_field(T, field);
        }
        w.raw("}\n\n");
    }

    // --- Serde field read (expression inside object literal) ---

    fn emit_serde_read_field(w: *Writer, comptime T: type, comptime field: std.builtin.Type.StructField) void {
        w.raw("    ");
        w.raw(field.name);
        w.raw(": ");

        if (is_array_type(field.type)) {
            const arr = @typeInfo(field.type).array;
            if (arr.child != u8) {
                // [N]T array → Array.from(...)
                const li = get_len_info(T, field.name);
                w.raw("Array.from({ length: ");
                w.emit_dv_read(li);
                w.raw(" }, (_, i) =>\n      read");
                w.raw(type_leaf_name(arr.child));
                w.raw("(data, offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(" + i * ");
                w.int(@sizeOf(arr.child));
                w.raw("))");
            } else if (has_len_companion(T, field.name)) {
                // [N]u8 with _len → string
                const li = get_len_info(T, field.name);
                w.raw("_decoder.decode(data.subarray(offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(", offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(" + ");
                w.emit_dv_read(li);
                w.raw("))");
            } else {
                // [N]u8 without _len → Uint8Array copy
                w.raw("data.slice(offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(", offset + ");
                w.int(@offsetOf(T, field.name) + arr.len);
                w.raw(")");
            }
        } else {
            w.emit_scalar_read(T, field);
        }

        w.raw(",\n");
    }

    fn emit_scalar_read(w: *Writer, comptime T: type, comptime field: std.builtin.Type.StructField) void {
        const ft = @typeInfo(field.type);
        switch (ft) {
            .int => |i| {
                if (i.bits == 128) {
                    w.raw("readU128(dv, offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(")");
                } else if (i.bits == 64) {
                    w.raw("Number(dv.getBig");
                    if (i.signedness == .signed) w.raw("Int64(offset + ") else w.raw("Uint64(offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(", true))");
                } else if (i.bits == 32) {
                    w.raw("dv.getUint32(offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(", true)");
                } else if (i.bits == 16) {
                    w.raw("dv.getUint16(offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(", true)");
                } else {
                    w.raw("dv.getUint8(offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(")");
                }
            },
            .@"enum" => {
                w.raw("readEnum<");
                w.raw(type_leaf_name(field.type));
                w.raw(">(dv, offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(", ");
                w.raw(type_leaf_name(field.type));
                w.raw("Values)");
            },
            .@"struct" => |s| {
                if (s.layout == .@"packed") {
                    w.raw("read");
                    w.raw(type_leaf_name(field.type));
                    w.raw("(dv, offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(")");
                } else {
                    assert(s.layout == .@"extern");
                    w.raw("read");
                    w.raw(type_leaf_name(field.type));
                    w.raw("(data, offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(")");
                }
            },
            .bool => {
                w.raw("dv.getUint8(offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(") !== 0");
            },
            else => @compileError("unhandled serde read type: " ++ @typeName(field.type)),
        }
    }

    // --- Serde field write (statements) ---

    fn emit_serde_write_field(w: *Writer, comptime T: type, comptime field: std.builtin.Type.StructField) void {
        if (is_array_type(field.type)) {
            const arr = @typeInfo(field.type).array;
            if (arr.child != u8) {
                // [N]T array — validate, loop, write length
                const li = get_len_info(T, field.name);
                const len_max = (@as(u64, 1) << li.bits) - 1;
                const arr_max = @min(arr.len, len_max);
                // Zero the full array region, then write populated items.
                w.raw("  data.fill(0, offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(", offset + ");
                w.int(@offsetOf(T, field.name) + arr.len * @sizeOf(arr.child));
                w.raw(");\n");
                w.raw("  if (val.");
                w.raw(field.name);
                w.raw(".length > ");
                w.int(arr_max);
                w.raw(") throw new Error('");
                w.raw(field.name);
                w.raw(" too long: ' + val.");
                w.raw(field.name);
                w.raw(".length);\n");
                w.raw("  for (let i = 0; i < val.");
                w.raw(field.name);
                w.raw(".length; i++) write");
                w.raw(type_leaf_name(arr.child));
                w.raw("(data, offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(" + i * ");
                w.int(@sizeOf(arr.child));
                w.raw(", val.");
                w.raw(field.name);
                w.raw("[i]);\n");
                // Write length
                w.raw("  ");
                w.emit_dv_write_start(li);
                w.raw("val.");
                w.raw(field.name);
                w.raw(".length");
                w.emit_dv_write_end(li);
                w.raw(";\n");
            } else if (has_len_companion(T, field.name)) {
                // [N]u8 with _len → string, with length validation
                const li = get_len_info(T, field.name);
                const len_max = (@as(u64, 1) << li.bits) - 1;
                // Zero the full buffer, encode string, validate + write length.
                w.raw("  data.fill(0, offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(", offset + ");
                w.int(@offsetOf(T, field.name) + arr.len);
                w.raw(");\n");
                w.raw("  { const _n = _encoder.encodeInto(val.");
                w.raw(field.name);
                w.raw(", data.subarray(offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(", offset + ");
                w.int(@offsetOf(T, field.name) + arr.len);
                w.raw(")).written; ");
                w.raw("if (_n > ");
                w.int(len_max);
                w.raw(") throw new Error('");
                w.raw(field.name);
                w.raw(" too long: ' + _n); ");
                w.emit_dv_write_start(li);
                w.raw("_n");
                w.emit_dv_write_end(li);
                w.raw("; }\n");
            } else {
                // [N]u8 without _len → Uint8Array
                w.raw("  data.set(val.");
                w.raw(field.name);
                w.raw(".subarray(0, ");
                w.int(arr.len);
                w.raw("), offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(");\n");
            }
        } else {
            w.emit_scalar_write(T, field);
        }
    }

    fn emit_scalar_write(w: *Writer, comptime T: type, comptime field: std.builtin.Type.StructField) void {
        const ft = @typeInfo(field.type);
        switch (ft) {
            .int => |i| {
                if (i.bits == 128) {
                    w.raw("  writeU128(dv, offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(", val.");
                    w.raw(field.name);
                    w.raw(");\n");
                } else if (i.bits == 64) {
                    w.raw("  dv.setBig");
                    if (i.signedness == .signed) w.raw("Int64(offset + ") else w.raw("Uint64(offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(", BigInt(val.");
                    w.raw(field.name);
                    w.raw("), true);\n");
                } else if (i.bits == 32) {
                    w.raw("  dv.setUint32(offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(", val.");
                    w.raw(field.name);
                    w.raw(", true);\n");
                } else if (i.bits == 16) {
                    w.raw("  dv.setUint16(offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(", val.");
                    w.raw(field.name);
                    w.raw(", true);\n");
                } else {
                    w.raw("  dv.setUint8(offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(", val.");
                    w.raw(field.name);
                    w.raw(");\n");
                }
            },
            .@"enum" => {
                w.raw("  writeEnum(dv, offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(", ");
                w.raw(type_leaf_name(field.type));
                w.raw("Values, val.");
                w.raw(field.name);
                w.raw(");\n");
            },
            .@"struct" => |s| {
                if (s.layout == .@"packed") {
                    w.raw("  write");
                    w.raw(type_leaf_name(field.type));
                    w.raw("(dv, offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(", val.");
                    w.raw(field.name);
                    w.raw(");\n");
                } else {
                    assert(s.layout == .@"extern");
                    w.raw("  write");
                    w.raw(type_leaf_name(field.type));
                    w.raw("(data, offset + ");
                    w.int(@offsetOf(T, field.name));
                    w.raw(", val.");
                    w.raw(field.name);
                    w.raw(");\n");
                }
            },
            .bool => {
                w.raw("  dv.setUint8(offset + ");
                w.int(@offsetOf(T, field.name));
                w.raw(", val.");
                w.raw(field.name);
                w.raw(" ? 1 : 0);\n");
            },
            else => @compileError("unhandled serde write type: " ++ @typeName(field.type)),
        }
    }

    // --- DataView read/write for _len companion fields ---

    fn emit_dv_read(w: *Writer, comptime li: LenInfo) void {
        switch (li.bits) {
            8 => {
                w.raw("dv.getUint8(offset + ");
                w.int(li.offset);
                w.raw(")");
            },
            16 => {
                w.raw("dv.getUint16(offset + ");
                w.int(li.offset);
                w.raw(", true)");
            },
            32 => {
                w.raw("dv.getUint32(offset + ");
                w.int(li.offset);
                w.raw(", true)");
            },
            else => unreachable,
        }
    }

    fn emit_dv_write_start(w: *Writer, comptime li: LenInfo) void {
        switch (li.bits) {
            8 => {
                w.raw("dv.setUint8(offset + ");
                w.int(li.offset);
                w.raw(", ");
            },
            16 => {
                w.raw("dv.setUint16(offset + ");
                w.int(li.offset);
                w.raw(", ");
            },
            32 => {
                w.raw("dv.setUint32(offset + ");
                w.int(li.offset);
                w.raw(", ");
            },
            else => unreachable,
        }
    }

    fn emit_dv_write_end(w: *Writer, comptime li: LenInfo) void {
        switch (li.bits) {
            8 => w.raw(")"),
            16, 32 => w.raw(", true)"),
            else => unreachable,
        }
    }
};

